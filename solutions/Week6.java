    public static int maxLemonPrice(int[] lemons) {
        if (lemons.length % 3 != 0)
            return -1;

        int sum = 0;
        Arrays.sort(lemons);
        for (int i = lemons.length - 2; i > (lemons.length / 3) - 1; i -= 2)
            sum += lemons[i];

        return sum;
    }

    public static void main(String[] args) {
        int[] a = {96, 104, 104, 104, 96, 104, 100, 100, 96, 97, 98, 98};
        maxLemonPrice(a); // 406
        int[] b = {1, 2, 3, 4, 5, 6};
        maxLemonPrice(b); // 8
        int[] c = {100, 200, 200};
        maxLemonPrice(c); // 200
        int[] d = {6, 11, 9, 12, 12};
        maxLemonPrice(d); // -1
    }
